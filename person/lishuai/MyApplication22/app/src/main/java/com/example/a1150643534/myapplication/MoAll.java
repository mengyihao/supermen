package com.example.a1150643534.myapplication;

import android.widget.ImageView;

public class MoAll {
    private Float x;
    private ImageView mo;
    private Float y;
    private int moHP;
    private int moVelocity;

    public int getMoVelocity() {
        return moVelocity;
    }

    public void setMoVelocity(int moVelocity) {
        this.moVelocity = moVelocity;
    }

    public int getMoHP() {
        return moHP;
    }

    public void setMoHP(int moHP) {
        this.moHP = moHP;
    }

    public Float getX() {
        return x;
    }

    public void setX(Float x) {
        this.x = x;
    }

    public ImageView getMo() {
        return mo;
    }

    public void setMo(ImageView mo) {
        this.mo = mo;
    }

    public Float getY() {
        return y;
    }

    public void setY(Float y) {
        this.y = y;
    }

}
